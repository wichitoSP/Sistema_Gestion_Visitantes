    private void crearResidenteModel() {
        
      modeloTabla.setRowCount(0);
      List<ResidenteModel> residentes = residenteDao.listarTodos();
      
        for (ResidenteModel r: residentes) {
            
            Object[] fila = {
                
                r.getId(),
                r.getNombre(),
                    r.getTelefono(),
                    r.getVivienda(),
                            
            
        };
            
            modeloTabla.addRow(fila);
            
          
           
                    
            
        }
       
      
        
       
    }

  
